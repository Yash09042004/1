--STRING FUNCTIONS
SELECT UPPER(NAME) AS UPPER_NAME,LOWER(NAME) AS LOWER_NAME,LENGTH(NAME) AS NAME_LENGTH,SUBSTRING(NAME, 1, 4) AS SUB_NAME,CONCAT(NAME, ' - ', 'Employee') AS FULL_DESCRIPTION FROM PERSON;

--AGGREGRATE FUNCTIONS
SELECT COUNT(*) AS TOTAL_PERSONS, SUM(SALARY) AS TOTAL_SALARY, AVG(SALARY) AS AVG_SALARY, MIN(SALARY) AS MIN_SALARY, MAX(SALARY) AS MAX_SALARY FROM PERSON;

